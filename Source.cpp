#include <iostream>
#include <math.h>
#include "Header.h"

double addition(double a, double b)
{
	return a + b;
}

double subtraction(double a, double b)
{
	return a - b;
}

double multiplying(double a, double b)
{
	return a * b;
}

double dividing(double a, double b)
{
	return a / b;
}

double power_numbers(double a, double b)
{
	return pow(a, b);
}